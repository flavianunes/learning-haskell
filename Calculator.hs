-- Basic calculator using Haskell by Flavia Nunes
module Calculator where
    add :: Double -> Double -> Double
    add a b = a + b

    sub :: Double -> Double -> Double
    sub a b = a - b

    mult :: Double -> Double -> Double
    mult a b = a * b

    half :: Double -> Double 
    half a = a / 2

    double :: Double -> Double
    double a = a * 2